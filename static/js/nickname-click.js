function fadeOut(el){
    el.style.opaticy = 1;

    (function fade(){
        if ( (el.style.opacity -= 0.1 ) < 0 ){
            $(el).removeClass("display-block");
        }
        else {
            requestAnimationFrame(fade);
        }
    })();
}

function fadeIn(el){
    el.style.opacity = 0;
    $(el).addClass("display-block");

    (function fade() {
        var val = parseFloat(el.style.opacity);
        if (!((val += 0.1) > 1)) {
            el.style.opacity = val;
            requestAnimationFrame(fade);
        }
    })();
}

